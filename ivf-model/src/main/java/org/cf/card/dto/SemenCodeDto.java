package org.cf.card.dto;

public class SemenCodeDto {

	private Long semenCodeId;
	
	private String dateOfUsed;
	
	private String remarks;
	
	public String getRemarks() {
		return remarks;
	}

	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}

	public Long getSemenCodeId() {
		return semenCodeId;
	}

	public void setSemenCodeId(Long semenCodeId) {
		this.semenCodeId = semenCodeId;
	}

	public String getDateOfUsed() {
		return dateOfUsed;
	}

	public void setDateOfUsed(String dateOfUsed) {
		this.dateOfUsed = dateOfUsed;
	}
	
	
	
}
